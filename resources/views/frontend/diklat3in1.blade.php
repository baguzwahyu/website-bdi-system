                Profesi (LSP),
                dan menyiapkan perangkat terkait. Setelah proses penyelenggaraan pelatihan dan sertifikasi dilakukan,
                maka proses terakhir adalah penempatan lulusan berdasarkan kerja sama yang telah disepakati dengan pihak
